import 'package:firebase_database/firebase_database.dart';

class Drink {
  final String id;
  final String image;
  final double price;
  final String name;
  final int quantity;
  final String description;

  Drink({
    required this.id,
    required this.image,
    required this.price,
    required this.name,
    required this.quantity,
    required this.description,
  });


}
